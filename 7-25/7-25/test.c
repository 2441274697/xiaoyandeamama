#define _CRT_SECURE_NO_WARNINGS 1
#include<stdio.h>


int main()
{
	//将数组的地址存放到数组指针p中
	int arr[5] = { 1,2,3,4,5 };
	int(*p)[5] = &arr;
	//*p―*：说明p是一个指针
	//p:数组指针的名称p
	//():由于[]的优先级比*高，若无()保护*，则为int *p[5]=&a;是指针数组
	//int:数组指针指向的数组元素的类型
	//[5]:数组指针指向的数组元素的个数
	//&arr:数组的地址
	char* ch[10];//创建一个指针数组ch，数组中存放着字符指针
	char*(*pch)[10] = &ch;
	//创建一个数组指针pch,存放指针数组ch的地址
	//():由于[]的优先级比*高，加()防止变为指针数组
	//*说明pch为指针
	//char*:数组指针pch指向的指针数组元素的类型为char*
	//[10]:数组指针pch指向的指针数组元素的个数为10

	return 0;
}

